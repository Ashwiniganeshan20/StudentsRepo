package com.cg.student;

public class Studenteg {
	int Rollno;
	String name;
	int mark;
	
	public Studenteg(int rollno, String name, int mark) {
		super();
		Rollno = rollno;
		this.name = name;
		this.mark = mark;
	}
	public int getRollno()
	{
		return Rollno;
	}
	public String getName()
	{
		return name;
	}
	public int getMark()
	{
		return mark;
	}



}
